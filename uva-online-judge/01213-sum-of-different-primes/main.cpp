long bag(int prime, int remN, int remK)
{
  if (remN < 0) return 0;
  if (remK == 0) return (remN == 0 ? 1 : 0);
  if (prime == NUM_PRIMES || primes[prime] > remN) return 0;
  if (memo[prime][remN][remK] != -1) return memo[prime][remN][remK];
  
  return memo[prime][remN][remK] = bag(prime + 1, remN-primes[prime], remK-1) +
                                   bag(prime + 1, remN, remK);
}

